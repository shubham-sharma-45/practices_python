#list append
add = [1,"shubham",3.2,34]
add.append(342)
print(add)

#list count
a = [ 1 ,2 ,2 ,3 ,1,2 ,4 ,55]
x = a.count(2)
print(x)

#list extent
fruits = ['apple', 'banana', 'cherry']
cars = ['Ford', 'BMW', 'Volvo']
fruits.extend(cars)
print(fruits)

#list index 
fruits = ['apple', 'banana', 'cherry','red']
x = fruits.index("red")
print(x)

#list pop 
a = [ 1 ,2 ,2 ,3 ,1,2 ,4 ,55]
a.pop()
print(a)

#list reverse
a = [ 1 ,2 ,2 ,3 ,1,2 ,4 ,55]
a.reverse()
print(a)


